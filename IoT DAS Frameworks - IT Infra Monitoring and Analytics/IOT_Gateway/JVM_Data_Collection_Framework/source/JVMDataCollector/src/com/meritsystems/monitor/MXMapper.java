package com.meritsystems.monitor;

import java.io.Serializable;

public class MXMapper implements Serializable {

    /**
     *
     */
    private static final long serialVersionUID = -3265569199350345359L;
    private String mx = null;
    private Class javaagentMX = null;
    private Class managementClass = null;

    /**
     * @param mx
     * @param javaagentMX
     * @param managementClass
     */
    public MXMapper(String mx, Class javaagentMX, Class managementClass) {
        super();
        this.mx = mx;
        this.javaagentMX = javaagentMX;
        this.managementClass = managementClass;
    }

    /**
     * @return
     */
    public String getMx() {
        return mx;
    }

    /**
     * @return
     */
    public Class getJavaagentMX() {
        return javaagentMX;
    }

    /**
     * @return
     */
    public Class getManagementClass() {
        return managementClass;
    }
}
